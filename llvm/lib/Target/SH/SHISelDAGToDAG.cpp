// Select address operands for load/store instructions.
// SH load/store addressing modes:
//   @Rn          — base register, zero offset
//   @(disp4, Rn) — base + 4-bit displacement * element size
bool SHDAGToDAGISel::SelectAddr(SDValue Addr, SDValue &Base, SDValue &Offset) {
  SDLoc DL(Addr);
  MVT VT = MVT::i32;

  // Frame index — let frame lowering handle the actual offset.
  if (auto *FIN = dyn_cast<FrameIndexSDNode>(Addr)) {
    Base = CurDAG->getTargetFrameIndex(FIN->getIndex(), VT);
    Offset = CurDAG->getTargetConstant(0, DL, VT);
    return true;
  }

  // base + constant displacement (4-bit * 4, i.e. 0..60 in steps of 4)
  if (Addr.getOpcode() == ISD::ADD) {
    if (auto *CN = dyn_cast<ConstantSDNode>(Addr.getOperand(1))) {
      int64_t Off = CN->getSExtValue();
      if (Off >= 0 && Off <= 60 && (Off & 3) == 0) {
        Base = Addr.getOperand(0);
        Offset = CurDAG->getTargetConstant(Off >> 2, DL, VT);
        return true;
      }
    }
  }

  // Default: base register, zero offset.
  Base = Addr;
  Offset = CurDAG->getTargetConstant(0, DL, VT);
  return true;
}

bool SHDAGToDAGISel::SelectInlineAsmMemoryOperand(
    const SDValue &Op, InlineAsm::ConstraintCode ConstraintID,
    std::vector<SDValue> &OutOps) {
  SDValue Base, Offset;
  if (!SelectAddr(Op, Base, Offset))
    return true;

  OutOps.push_back(Base);
  OutOps.push_back(Offset);
  return false;
}

void SHDAGToDAGISel::Select(SDNode *N) {
  // If we have a custom node, we already have selected!
  if (N->isMachineOpcode()) {
    N->setNodeId(-1);
    return;
  }

  // Handle custom SHISD opcodes.
  switch (N->getOpcode()) {
  case SHISD::BRCC: {
    SDValue Chain = N->getOperand(0);
    SDValue Dest = N->getOperand(1);
    SDValue LHS = N->getOperand(2);
    SDValue RHS = N->getOperand(3);
    SDValue CC = N->getOperand(4);

    SDValue Ops[] = {LHS, RHS, CC, Dest, Chain};
    CurDAG->SelectNodeTo(N, SH::BRCC, MVT::Other, Ops);
    return;
  }
  case SHISD::CMP: {
    SDValue LHS = N->getOperand(0);
    SDValue RHS = N->getOperand(1);
    unsigned CC = cast<ConstantSDNode>(N->getOperand(2))->getZExtValue();

    unsigned CmpOpc = 0;
    if (LHS.getValueType() == MVT::f32) {
      if (CC == SHCC::EQ)
        CmpOpc = SH::FCMP_EQ_S;
      else if (CC == SHCC::GT)
        CmpOpc = SH::FCMP_GT_S;
      else if (CC == SHCC::LT) {
        CmpOpc = SH::FCMP_GT_S;
        std::swap(LHS, RHS);
      } else
        llvm_unreachable("Invalid SHCC in SHISD::CMP for f32");
    } else if (LHS.getValueType() == MVT::f64) {
      if (CC == SHCC::EQ)
        CmpOpc = SH::FCMP_EQ_D;
      else if (CC == SHCC::GT)
        CmpOpc = SH::FCMP_GT_D;
      else if (CC == SHCC::LT) {
        CmpOpc = SH::FCMP_GT_D;
        std::swap(LHS, RHS);
      } else
        llvm_unreachable("Invalid SHCC in SHISD::CMP for f64");
    } else {
      // SH has CMP/EQ, CMP/GT, CMP/GE, CMP/HI, CMP/HS but no
      // CMP/LT or CMP/LE.  LT/LE are handled by swapping operands.
      switch ((SHCC::CondCode)CC) {
      case SHCC::EQ:
        CmpOpc = SH::CMP_EQ;
        break;
      case SHCC::GT:
        CmpOpc = SH::CMP_GT;
        break;
      case SHCC::GE:
        CmpOpc = SH::CMP_GE;
        break;
      case SHCC::LT:
        CmpOpc = SH::CMP_GT;
        std::swap(LHS, RHS);
        break;
      case SHCC::LE:
        CmpOpc = SH::CMP_GE;
        std::swap(LHS, RHS);
        break;
      case SHCC::UGT:
        CmpOpc = SH::CMP_HI;
        break;
      case SHCC::UGE:
        CmpOpc = SH::CMP_HS;
        break;
      case SHCC::ULT:
        CmpOpc = SH::CMP_HI;
        std::swap(LHS, RHS);
        break;
      case SHCC::ULE:
        CmpOpc = SH::CMP_HS;
        std::swap(LHS, RHS);
        break;
      default:
        llvm_unreachable("Invalid SHCC in SHISD::CMP");
      }
    }

    SDLoc DL(N);
    if (auto *CN = dyn_cast<ConstantSDNode>(LHS)) {
      LHS =
          SDValue(CurDAG->getMachineNode(SH::MOV_I8, DL, MVT::i32,
                                         CurDAG->getTargetConstant(
                                             CN->getSExtValue(), DL, MVT::i32)),
                  0);
    }
    if (auto *CN = dyn_cast<ConstantSDNode>(RHS)) {
      RHS =
          SDValue(CurDAG->getMachineNode(SH::MOV_I8, DL, MVT::i32,
                                         CurDAG->getTargetConstant(
                                             CN->getSExtValue(), DL, MVT::i32)),
                  0);
    }

    SDValue Ops[] = {LHS, RHS};
    CurDAG->SelectNodeTo(N, CmpOpc, MVT::Glue, Ops);
    return;
  }
  case SHISD::MOVT: {
    SDValue Glue = N->getOperand(0);
    SDValue Ops[] = {Glue};
    CurDAG->SelectNodeTo(N, SH::MOVT, MVT::i32, Ops);
    return;
  }
  case SHISD::GOTAddr: {
    // GOTAddr(CPI) → MOV_GOT CPI
    // The CPI operand is a TargetConstantPool pointing to
    // _GLOBAL_OFFSET_TABLE_@GOTPC.
    SDValue CPOp = N->getOperand(0);
    CurDAG->SelectNodeTo(N, SH::MOV_GOT, MVT::i32, CPOp);
    return;
  }
  // ISD::LOAD and ISD::STORE removed to rely on TableGen where address offsets
  // correctly match.
  default:
    break;
  }

  SelectCode(N);
}

FunctionPass *llvm::createSHISelDag(SHTargetMachine &TM,
                                    CodeGenOptLevel OptLevel) {
  return new SHDAGToDAGISelLegacy(TM, OptLevel);
}
