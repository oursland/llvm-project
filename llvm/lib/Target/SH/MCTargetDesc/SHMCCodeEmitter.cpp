#include "MCTargetDesc/SHMCTargetDesc.h"
#include "llvm/MC/MCCodeEmitter.h"
#include "llvm/MC/MCContext.h"
#include "llvm/MC/MCExpr.h"
#include "llvm/MC/MCFixup.h"
#include "llvm/MC/MCInst.h"
#include "llvm/MC/MCInstrInfo.h"
#include "llvm/MC/MCRegisterInfo.h"
#include "llvm/MC/MCSubtargetInfo.h"
#include "llvm/Support/EndianStream.h"

#include "SHFixupKinds.h"

using namespace llvm;

#define DEBUG_TYPE "mccodeemitter"

namespace {
class SHMCCodeEmitter : public MCCodeEmitter {
  MCContext &Ctx;

public:
  SHMCCodeEmitter(const MCInstrInfo &mcii, MCContext &ctx) : Ctx(ctx) {
    (void)mcii;
  }

  ~SHMCCodeEmitter() override = default;

  // This function is auto-generated by TableGen!
  uint64_t getBinaryCodeForInstr(const MCInst &MI,
                                 SmallVectorImpl<MCFixup> &Fixups,
                                 const MCSubtargetInfo &STI) const;

  // Extracts the binary value of an operand (e.g., register number)
  unsigned getMachineOpValue(const MCInst &MI, const MCOperand &MO,
                             SmallVectorImpl<MCFixup> &Fixups,
                             const MCSubtargetInfo &STI) const;

  unsigned encodeBranchTarget12(const MCInst &MI, unsigned OpNo,
                                SmallVectorImpl<MCFixup> &Fixups,
                                const MCSubtargetInfo &STI) const;

  unsigned encodeBranchTarget8(const MCInst &MI, unsigned OpNo,
                               SmallVectorImpl<MCFixup> &Fixups,
                               const MCSubtargetInfo &STI) const;

  // The main entry point for encoding an instruction
  void encodeInstruction(const MCInst &MI, SmallVectorImpl<char> &CB,
                         SmallVectorImpl<MCFixup> &Fixups,
                         const MCSubtargetInfo &STI) const override;
};
} // end anonymous namespace

unsigned
SHMCCodeEmitter::encodeBranchTarget12(const MCInst &MI, unsigned OpNo,
                                      SmallVectorImpl<MCFixup> &Fixups,
                                      const MCSubtargetInfo &STI) const {
  const MCOperand &MO = MI.getOperand(OpNo);
  if (MO.isImm())
    return MO.getImm();
  if (MO.isExpr()) {
    Fixups.push_back(MCFixup::create(0, MO.getExpr(),
                                     MCFixupKind(SH::fixup_sh_pcrel12), true));
    return 0;
  }
  llvm_unreachable("Unhandled operand kind in encodeBranchTarget12!");
}

unsigned
SHMCCodeEmitter::encodeBranchTarget8(const MCInst &MI, unsigned OpNo,
                                     SmallVectorImpl<MCFixup> &Fixups,
                                     const MCSubtargetInfo &STI) const {
  const MCOperand &MO = MI.getOperand(OpNo);
  if (MO.isImm())
    return MO.getImm();
  if (MO.isExpr()) {
    Fixups.push_back(MCFixup::create(0, MO.getExpr(),
                                     MCFixupKind(SH::fixup_sh_pcrel8), true));
    return 0;
  }
  llvm_unreachable("Unhandled operand kind in encodeBranchTarget8!");
}

unsigned SHMCCodeEmitter::getMachineOpValue(const MCInst &MI,
                                            const MCOperand &MO,
                                            SmallVectorImpl<MCFixup> &Fixups,
                                            const MCSubtargetInfo &STI) const {
  if (MO.isReg())
    return Ctx.getRegisterInfo()->getEncodingValue(MO.getReg());
  if (MO.isImm())
    return static_cast<unsigned>(MO.getImm());

  if (MO.isExpr()) {
    Fixups.push_back(MCFixup::create(0, MO.getExpr(),
                                     MCFixupKind(SH::fixup_sh_pcrel_m8), true));
    return 0;
  }

  return 0;
}

void SHMCCodeEmitter::encodeInstruction(const MCInst &MI,
                                        SmallVectorImpl<char> &CB,
                                        SmallVectorImpl<MCFixup> &Fixups,
                                        const MCSubtargetInfo &STI) const {
  // Get the 16-bit encoded instruction from the auto-generated TableGen
  // function
  uint16_t Bits = getBinaryCodeForInstr(MI, Fixups, STI);

  // SH is typically little-endian for instructions, write the 16 bits to the
  // buffer
  support::endian::write<uint16_t>(CB, Bits, llvm::endianness::little);
}

// Include the auto-generated code
#include "SHGenMCCodeEmitter.inc"

// Define the creation function we'll use in the Target Registry
MCCodeEmitter *llvm::createSHMCCodeEmitter(const MCInstrInfo &MCII,
                                           MCContext &Ctx) {
  return new SHMCCodeEmitter(MCII, Ctx);
}
